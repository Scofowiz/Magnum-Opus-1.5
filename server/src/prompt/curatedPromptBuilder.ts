function clip(text: string, maxChars: number): string {
  if (maxChars <= 0) return "";
  if (text.length <= maxChars) return text;
  return `${text.slice(0, maxChars - 1).trim()}...`;
}

function clipTail(text: string, maxChars: number): string {
  if (maxChars <= 0) return "";
  if (text.length <= maxChars) return text;
  return `...${text.slice(text.length - maxChars + 3).trim()}`;
}

function cleanLineBreaks(text: string): string {
  return text
    .replace(/\r/g, "")
    .replace(/[ \t]+\n/g, "\n")
    .replace(/\n{3,}/g, "\n\n")
    .trim();
}

function normalizeNarrativeText(text: string): string {
  if (!text) return "";
  try {
    if (/[<>]/.test(text)) {
      return cleanLineBreaks(htmlToPlainText(text, { wordwrap: false }));
    }
  } catch {
    return cleanLineBreaks(text.replace(/<[^>]+>/g, " "));
  }
  return cleanLineBreaks(text);
}

function tokenize(text: string): Set<string> {
  return new Set(
    text
      .toLowerCase()
      .replace(/[^a-z0-9\s]/g, " ")
      .split(/\s+/)
      .filter((token) => token.length > 2),
  );
}

function lexicalOverlap(a: string, b: string): number {
  const left = tokenize(a);
  const right = tokenize(b);
  if (left.size === 0 || right.size === 0) return 0;

  let overlap = 0;
  for (const token of left) {
    if (right.has(token)) overlap++;
  }

  return overlap / Math.max(left.size, right.size);
}

function escapeRegExp(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function isNumericCodename(value: string): boolean {
  return /^(zero|one|two|three|four|five|six|seven|eight|nine|ten|eleven|twelve|thirteen|fourteen|fifteen|\d+)$/i.test(
    value.trim(),
  );
}

function buildAliasPattern(alias: string): RegExp | null {
  const normalized = alias.trim();
  if (!normalized) return null;

  const escaped = escapeRegExp(normalized).replace(/\s+/g, "\\s+");
  if (isNumericCodename(normalized)) {
    return new RegExp(`(?<!asset\\s)(?<!agent\\s)\\b${escaped}\\b`, "i");
  }

  return new RegExp(`\\b${escaped}\\b`, "i");
}

function aliasMentions(text: string, aliases: string[]): number {
  const haystack = text.toLowerCase();
  let hits = 0;

  for (const alias of aliases) {
    const normalized = alias.trim().toLowerCase();
    if (!normalized) continue;
    if (normalized.length < 3 && !isNumericCodename(normalized)) continue;
    const pattern = buildAliasPattern(normalized);
    if (pattern?.test(haystack)) hits++;
  }

  return hits;
}

function findCurrentChapter(
  project: PromptProject,
  chapterId?: string,
): PromptChapter | undefined {
  if (!chapterId) return undefined;
  return project.chapters.find((chapter) => chapter.id === chapterId);
}

